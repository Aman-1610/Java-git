public class Nestedinvertedtringlepattern {
    public static void main(String args[])
     {
         int i,j;
         for(i=0;i<6;i++)
         {
            for(j=i;j<6;j++)
            {
                System.out.print("*");
            }
            System.out.println(" ");
         }
     }
}
